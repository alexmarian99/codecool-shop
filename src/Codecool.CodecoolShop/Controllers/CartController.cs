using Codecool.CodecoolShop.Daos.Implementations;
using Codecool.CodecoolShop.Models;
using Codecool.CodecoolShop.Services;
using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Logging;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace Codecool.CodecoolShop.Controllers
{
    public class CartController : Controller
    {
        public ProductService ProductService { get; set; }
        private readonly ILogger<ProductController> _logger;


        public CartController(ILogger<ProductController> logger)
        {
            _logger = logger;
            ProductService = new ProductService(
                ProductDaoMemory.GetInstance(),
                ProductCategoryDaoMemory.GetInstance(),
                SupplierDaoMemory.GetInstance(),                
                UserDaoMemory.GetInstance());
            
                
        }
        // GET: CartController
        public ActionResult Index(User user)
        {
            
            Cart cart = ProductService.GetCart(user);
            return View(cart);
        }

        // GET: CartController/Details/5
        public ActionResult AddToCart(int ProductId,User user)
        {
            var products = ProductService.GetAllProducts() ;
            foreach (var product in products)
            {
                if (product.Id == ProductId)
                {
                    ProductService.GetCart(user).Add(product);
                }
            }

            return Redirect("/");
        }

        // GET: CartController/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: CartController/Create
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create(IFormCollection collection)
        {
